/*
  Bloque 2. Ejercicio 6:
  Escriba un programa que lea la nota final de cuatro alumnos y calcula la nota final media de los cuatro alumnos.
*/

#include <iostream>

using namespace std;

int main()
{
	float alumno1, alumno2, alumno3, alumno4;
	float notafinal;
	float calificacionMedia;
	
	cout << "Ingresa la calificacion del primer alumno: ";
	cin >> alumno1;
	
	cout << "Ingrese la calificcion del segundo alumno: ";
	cin >> alumno2;
	
	cout << "Ingrese la calificacion del tercer alumno: ";
	cin >> alumno3;
	
	cout << "Ingrese la calificacion del cuarto alumno: ";
	cin >> alumno4;
	
	notafinal = alumno1 + alumno2 + alumno3 + alumno4;
	
	calificacionMedia = notafinal / 4;
	
	cout << "\nLa media de las calificaciones es: " << calificacionMedia << endl;
	
	return 0;
}
